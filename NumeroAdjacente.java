public class NumeroAdjacente {

    public static void main (String[] args){

        int a = 1;
        int b = 2;
        int c = 3;
        int d = 4;

        System.out.println(a + " " + b + " " + c + " " + d + "\n");
        System.out.printf("%d %d %d %d",
                a, b, c, d);

    }
}